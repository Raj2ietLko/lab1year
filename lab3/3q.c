#include <stdio.h>
#include <math.h>


int main()
{
    int a=3,b=3;
    while(a)(b==1)? (printf("%d\n",b),a--,b=a) : (printf("%d ",b),b--) ;
    a=b=1;
    while(a<4)(b==a)? (printf("*\n"),a++,b=1) : (printf("*"),b++) ;    
}